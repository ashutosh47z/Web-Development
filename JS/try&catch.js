// try and catch in Java Script
/* try statement -> Try statement allows you to define a block of code to be tested for errors while it is being executed.
catch statement -> The catch statement allows you to define a block of code to be executed, if an error occurs in the try block.
*/
console.log("Hello");
try {
    console.log(a);
} catch (err) {
    console.log("The variable 'a' does not exist");
    console.log(err);
}
console.log("Hello! Ashutosh this side");
console.log("My nationality is Indian");
