// Math Object(Predefined) in JavaScript
/* 
Properties
1. Math.PI
2. Math.E
Methods of Math Object
1. Math.abs()-> Returns the absolute value of a number or Modulus of Number
2. Math.pow(number, power)-> Returns the power of a number as an output
3. Math.floor(number)-> Round off the number and returns number less than or equal to the input number
4. Math.ceil(number) -> Round off the given number to number just larger than input
5. Math.random() -> Returns a random number between 0 and 1 but 1 is exclusive
*/
console.log(Math.pow(2, 4));  //Power Method
console.log(Math.abs(-14));   //Absolute Method
console.log(Math.floor(2.9999999)); //Floor Method
console.log(Math.ceil(2.00000001)); //Ceil Method
console.log(Math.random()); //Random Method